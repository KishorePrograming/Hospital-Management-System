    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        try {
            String d = txtDrugCode.getSelectedItem().toString();
            pst = con.prepareStatement("Select * from items where itemno = ? ");
            pst.setString(1, d);
            rs = pst.executeQuery();
            int fQty = Integer.parseInt(txtQty.getValue().toString());
            while(rs.next()){
                int currentQty = rs.getInt("qty");
                int sellPrice = rs.getInt("sellprice");
                
                if(fQty>currentQty){
                    JOptionPane.showMessageDialog(this, "Available Qty is : "+currentQty);
                    JOptionPane.showMessageDialog(this, "Qty Not Enough");
                    txtQty.requestFocus();
                }else if(0<fQty&&fQty<=currentQty){
                    int total = fQty*sellPrice;
                    DefaultTableModel df = (DefaultTableModel)jtbleDrug.getModel();
                    df.addRow(new Object[]{
                        lblPno.getText(),
                        txtDrugCode.getSelectedItem().toString(),
                        txtDrugName.getText(),
                        txtQty.getValue().toString(),
                        sellPrice,
                        total
                    });
                    int sum=0;
                    for(int i=0;i<jtbleDrug.getRowCount();i++){
                      sum+=Integer.parseInt(jtbleDrug.getValueAt(i, 5).toString());
                    }
                    txtTotalCost.setText(Integer.toString(sum));
                    btnSalesUpdate.setEnabled(true);
                    txtPay.setEditable(true);
                    btnClear.setEnabled(false);
                }else{
                    JOptionPane.showMessageDialog(this, "Available Qty is : "+currentQty);
                    JOptionPane.showMessageDialog(this, "Qty MissMatch");
                    txtQty.requestFocus();
                }
                
            }
            txtPay.requestFocus();
        }catch(SQLException e){
            
        }
    }//GEN-LAST:event_btnaddActionPerformed
    
    public static boolean numCheck(JTextField txt){
        boolean flag = false;
        String s = txt.getText();
        for(int i=0;i<s.length();i++){
            if('0'<=s.charAt(i)&&s.charAt(i)<='9'){
                flag=true;
            }else{
                flag= false;
            }
        }
        return flag;
    }
    
