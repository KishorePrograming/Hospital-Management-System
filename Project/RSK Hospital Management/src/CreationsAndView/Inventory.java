    public void drugLoader(){
        try {
            String sql = "Select * from Items";
            txtDrugCode.removeAll();
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            while(rs.next()){
                txtDrugCode.addItem(rs.getString(1));               
                }
        } catch (SQLException ex) {
            Logger.getLogger(ChannelCreation.class.getName()).log(Level.SEVERE, null, ex);
        }
        AutoCompleteDecorator.decorate(txtDrugCode);
    }
   

    public void DrugCheckput(){
        try {
            String d = txtDrugCode.getSelectedItem().toString();
            pst = con.prepareStatement("Select * from items where itemno = ? ");
            pst.setString(1, d);
            rs = pst.executeQuery();
            if(rs.next()==false){
                JOptionPane.showMessageDialog(this, "Drug Not found");
                txtDrugCode.setSelectedItem(-1);
                txtDrugCode.requestFocus();
            }else{
             String dname = rs.getString("itemname");
             txtDrugName.setText(dname.trim());
             txtDrugName.setEditable(false);
             txtQty.requestFocus();
            }
           } catch (SQLException e) {
               JOptionPane.showMessageDialog(this, "Something went wrong Drugs");
        }
    }
 

