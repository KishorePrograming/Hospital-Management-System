    public void autoId(){
        try {
             Statement st = con.createStatement();
             rs = st.executeQuery("select MAX(itemno) from Items");
             rs.next();            
             rs.getString("MAX(itemno)");
             if(rs.getString("MAX(itemno)")==null){
                 lblItemId.setText("IT001");
             }else{
                 long id = Long.parseLong(rs.getString("MAX(itemno)").substring(2, rs.getString("MAX(itemno)").length()));
                 id++;
                 lblItemId.setText("IT"+String.format("%03d", id));
             }
             
        } catch (SQLException e) {
        }
    }
