    private void itemsTable(){
        try {
            pst = con.prepareStatement("select * from Items");
            rs = pst.executeQuery();
            ResultSetMetaData rsm = rs.getMetaData();
            int c = rsm.getColumnCount();
            DefaultTableModel df = (DefaultTableModel)tblItem.getModel();
            df.setRowCount(0);
            while(rs.next()){
                Vector v = new Vector();
                for(int i=1;i<=c;i++){
                    v.add(rs.getString("itemno"));
                    v.add(rs.getString("itemname"));
                    v.add(rs.getString("description"));
                    v.add(rs.getString("sellprice"));
                    v.add(rs.getString("buyprice"));
                    v.add(rs.getString("qty"));
                }
                df.addRow(v);
            }
          
            
        } catch (SQLException e) {
        }
    }
    private boolean IntnumCheck(){
        boolean flag = false;
        if(numCheck(txtSellPrice)){
            if(numCheck(txtBuyPrice)){
                if(numCheck(txtQty)){
                      if(numCheck(txtSellPrice)&&numCheck(txtBuyPrice)&&numCheck(txtQty)){
                          flag = true;
                        }else{
                        JOptionPane.showMessageDialog(this,"Please Give Everything correctly");
                        txtSellPrice.setText("");
                        txtBuyPrice.setText("");
                        txtQty.setText("");
                        txtSellPrice.requestFocus(); 
                      }
                }else{
                    JOptionPane.showMessageDialog(this,"Please Give Correct Input in Qty");
                    txtQty.setText("");
                }
            }else{
                JOptionPane.showMessageDialog(this,"Please Give Correct Input in BuyPrice ");
                    txtBuyPrice.setText("");
            }
        }else{
            JOptionPane.showMessageDialog(this,"Please Give Correct Input in SellPrice ");
            txtSellPrice.setText("");
        }
        return flag;
    }
    public boolean numCheck(JTextField txt){
        
        String s = txt.getText();
        boolean flag = false;
        for(int i=0;i<s.length();i++){
            if('0'<=s.charAt(i)&&s.charAt(i)<='9'){
                flag=true;
            }else{
                flag= false;
            }
        }
        return flag;
    }
    
