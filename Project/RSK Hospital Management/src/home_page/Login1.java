    public static void checkUserType(int userid,String username,String usertype,Connection con){
            System.gc();
        switch (usertype) {
//            Pharmacist, Doctor, Receptionist
            
            case "Doctor" -> new MainDoctor(userid,username,usertype,con).setVisible(true);
            case "Receptionist" -> new MainReceptionist(userid,username,usertype,con).setVisible(true);
            case "Pharmacist" -> new MainPharmacist(userid,username,usertype,con).setVisible(true);
            default -> {
                System.out.println("Error");
            }
        }
    }
