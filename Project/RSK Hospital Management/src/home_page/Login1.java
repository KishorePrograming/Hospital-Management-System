    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
        if(!txtusername.getText().equals("")&&!txtpassword.getText().equals("")){
                    String fusername = txtusername.getText();
                    String fpassword = txtpassword.getText();
                    String fusertype = txtusertype.getSelectedItem().toString();
        try {
            String sql = "select * from user where username=? and password=? and usertype=?;";
            pst = con.prepareStatement(sql);
            pst.setString(1, fusername);
            pst.setString(2, fpassword);
            pst.setString(3, fusertype);
            rs = pst.executeQuery();
            boolean check = rs.next(); 
            if(check==true){
                int duserid = rs.getInt("id");
                String dusername = rs.getString("username");
                String dpassword = rs.getString("password");
                String dusertype = rs.getString("usertype");
                System.out.println(" d "+duserid+" f "+fusername+" d "+dpassword+" f "+fpassword+" d "+dusertype+" f "+fusertype);
                  if((dusername.equals(fusername))&&(dpassword.equals(fpassword))&&(dusertype.equals(fusertype))){
                     this.setVisible(false);
//                     RedirectingUserType
                    checkUserType(duserid,dusername,dusertype,con);
                  }else{
                wrongPassword();
            }   
            }else if(check==false){
                wrongPassword();
            }
          } catch (SQLException ex) {
            Logger.getLogger(Login1.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        else{
            JOptionPane.showMessageDialog(this,"Please fill Everything...");
            
        }
        
      
    }//GEN-LAST:event_btnloginActionPerformed

